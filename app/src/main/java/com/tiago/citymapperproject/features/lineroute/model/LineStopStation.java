package com.tiago.citymapperproject.features.lineroute.model;

public class LineStopStation {

  private String id;
  private String name;
  private Double lat;
  private Double lon;

  public String getId() {
    return id;
  }

  public String getName() {
    return name;
  }

  public Double getLat() {
    return lat;
  }

  public Double getLon() {
    return lon;
  }
}
